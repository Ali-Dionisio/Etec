package calculadora;

public class OpAritm {

	public float soma(float n1, float n2) {
		return (n1 + n2);
	}
	public float subtrai(float n1, float n2) {
		return (n1 - n2);
	}
	public float divide(float n1, float n2) {
		return (n1 / n2);
	}
	public float multiplica(float n1, float n2) {
		return (n1 * n2);
	}
}
